
public class CellPhone {
	private String name;
	private String price;
	private String URL;
	public CellPhone(){
		name=null;
		price=null;
		URL=null;
		
	}
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	String getPrice() {
		return price;
	}
	void setPrice(String price) {
		this.price = price;
	}
	String getURL() {
		return URL;
	}
	void setURL(String uRL) {
		URL = uRL;
	}

}
